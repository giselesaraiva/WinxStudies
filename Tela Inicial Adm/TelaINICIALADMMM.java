 private void openMenuBar(){
        new Thread(new Runnable() {
            @Override
            public void run() {
                for(int i=0;i<w;i++){
                    menu1.setSize(w, h);
                    try {
                        Thread.sleep(1);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(TelaINICIALADMMM.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
}
       private void closeMenuBar(){
        new Thread(new Runnable() {
            @Override
            public void run() {
                for(int i=w;i>0;i--){
                    menu1.setSize(i, h);
                    try {
                        Thread.sleep(1);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(TelaINICIALADMMM.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        }).start();
}
